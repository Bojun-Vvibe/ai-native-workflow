def validate_records(records: Iterable[dict[str, Any]]) -> dict[str, Any]:
    """Validate a batch of span records. Returns a typed report.

    Checks:
      - Exactly one root per trace_id (parent == ZERO_SPAN).
      - Every non-root parent_span_id is present in the same trace.
      - No span_id collisions within a trace.
      - No span is its own parent.
      - Every span has finite duration if status != in_progress.
    """
    records = list(records)
    by_trace: dict[str, list[dict[str, Any]]] = {}
    for r in records:
        by_trace.setdefault(r["trace_id"], []).append(r)

    errors: list[dict[str, Any]] = []
    for trace_id, rs in by_trace.items():
        ids = [r["span_id"] for r in rs]
        if len(set(ids)) != len(ids):
            dupes = sorted({s for s in ids if ids.count(s) > 1})
            errors.append(
                {"code": "duplicate_span_id", "trace_id": trace_id, "span_ids": dupes}
            )
        roots = [r for r in rs if r["parent_span_id"] == ZERO_SPAN]
        if len(roots) != 1:
            errors.append(
                {"code": "wrong_root_count", "trace_id": trace_id, "found": len(roots)}
            )
        id_set = set(ids)
        for r in rs:
            if r["span_id"] == r["parent_span_id"]:
                errors.append(
                    {
                        "code": "self_parent",
                        "trace_id": trace_id,
                        "span_id": r["span_id"],
                    }
                )
            if (
                r["parent_span_id"] != ZERO_SPAN
                and r["parent_span_id"] not in id_set
            ):
                errors.append(
                    {
                        "code": "orphan_span",
                        "trace_id": trace_id,
                        "span_id": r["span_id"],
                        "parent_span_id": r["parent_span_id"],
                    }
                )
            if r["status"] != "in_progress" and r["duration_ms"] is None:
                errors.append(
                    {
                        "code": "missing_duration",
                        "trace_id": trace_id,
                        "span_id": r["span_id"],
                    }
                )
    return {"ok": not errors, "trace_count": len(by_trace), "errors": errors}


def render_tree(tree: list[dict[str, Any]]) -> str:
    """ASCII tree for human eyes. Stable string."""
    lines = []
    for r in tree:
        prefix = "  " * r["depth"] + ("- " if r["depth"] else "* ")
        meta = (
            f"[{r['status']}, {r['duration_ms']}ms"
            f"{', synthetic' if r.get('synthetic') else ''}]"
        )
        lines.append(f"{prefix}{r['name']} {meta} span={r['span_id'][:8]}")
    return "\n".join(lines)


